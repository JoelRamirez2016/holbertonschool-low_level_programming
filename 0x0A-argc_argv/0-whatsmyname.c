#include <stdio.h>

/**
 * main - entry point
 * Description: prints its name
 * @argc: size of program parameters in argv, (unused)
 * @argv: array of parameters to the program
 * Return: Always 0
 */

int main(int __attribute__((unused)) argc, char *argv[])
{
	printf("%s\n", argv[0]);
	return (0);
}
