#include "calc.h"

/**
 * op_add - add two numbers
 * @a: The first number
 * @b: The second number
 * Return: The sum of a and b
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtract two numbers
 * @a: The first number
 * @b: The second number
 * Return: The subtract of a and b
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiply two numbers
 * @a: The first number
 * @b: The second number
 * Return: The multiply of a and b
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divide two numbers
 * @a: The first number
 * @b: The second number
 * Return: The divide of a and b
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - divide two numbers and getting the module
 * @a: The first number
 * @b: The second number
 * Return: The module of a and b
 */

int op_mod(int a, int b)
{
	return (a % b);
}
